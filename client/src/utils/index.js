export const randomImg= (type, parksImg, barsImg) =>{
  switch (type) {
    case 'park':
      return parksImg[Math.floor(Math.random() * parksImg.length)];
    case 'bar':
      return barsImg[Math.floor(Math.random() * barsImg.length)];
    default:
      break;
  }
};

